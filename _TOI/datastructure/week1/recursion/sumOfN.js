//find sum of n odd

function sumOfNodd(n) {
    return n * n
}

console.log(sumOfNodd(5));


//find sum of n even

function sumOfNeven(n) {
    return n * (n + 1)
}

console.log(sumOfNeven(5));


//find sum of n numbers

function sumOfNnatural(n) {
    return (n*(n+1))/2
}

console.log(sumOfNnatural(5))